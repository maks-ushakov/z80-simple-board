MemoryLoader::MemoryLoader(uint16_t maxAddr) {
  _maxAddr = maxAddr;
};

void MemoryLoader::init(CPUBoard * board) {
  _board = board;
};

uint8_t MemoryLoader::checkAddr(uint16_t addr) {
  return addr < _maxAddr ? 1 : 0;
};

void MemoryLoader::loadChunk(MemoryChunk chunk) {
  uint16_t size = chunk.size;
  uint16_t addr = chunk.startAddress;
  const uint8_t * ptr = chunk.memChunk;
  for (uint16_t i = 0; i < size; i++) {
    setByte(addr + i, pgm_read_byte(ptr + i));
  }
}


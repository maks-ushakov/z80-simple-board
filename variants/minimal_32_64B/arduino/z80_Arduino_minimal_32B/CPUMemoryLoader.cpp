uint8_t MemoryLoader::getByte(uint16_t addr) {
   return checkAddr(addr) ? mockRAM[addr % (_maxAddr + 1)] : 0xff;
};

uint8_t MemoryLoader::setByte(uint16_t addr, uint8_t data) {
  if (checkAddr(addr)) {
    mockRAM[addr % (_maxAddr + 1)] = data;
    return 0;
  }
  return 0xff;
};

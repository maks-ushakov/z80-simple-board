uint8_t MemoryLoader::setBlock(uint16_t addr, uint8_t* buffer, uint16_t size, bool pgmem) {
  if (pgmem == 1) {
    return 0;
  }
  for(int i = 0; i < size; i++) {
    mockRAM[addr + i] = buffer[i];
  }
  return 0;
};
